    void populateTable()
    {
        try
        {
            String cols[] = {"ID","Title","Author","Publisher","Price","Stack No.","Copies","Books issued"};
            
            String qry = "select count(*)NOR from tbbook";
            DB obj = new DB();
            ResultSet rs = obj.execute(qry);
            rs.next();
            
            int rows = rs.getInt("NOR");
            String data[][] = new String[rows][8];
            rs = obj.execute("select * from tbbook");
            int i =0,j =0;
            
            while(rs.next())
            {
                data[i][j++] = rs.getString("bookid");
                data[i][j++] = rs.getString("booktit");
                data[i][j++] = rs.getString("bookaut");
                data[i][j++] = rs.getString("bookpub");
                data[i][j++] = rs.getString("bookprc");
                data[i][j++] = rs.getString("bookstackno");
                data[i][j++] = rs.getString("bookcopies");
                data[i][j++] = rs.getString("booksissued");
                j = 0;
                i++;
            }
            
            DefaultTableModel dtm = new DefaultTableModel(data,cols);
            jTable1.setModel(dtm);
        }
        
        catch(Exception e)
        {
            System.out.println(e);
        }
    }

