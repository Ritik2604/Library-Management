    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try
        {
            String pwd = jPasswordField1.getText();
            String cpwd = jPasswordField2.getText();

            if(pwd.equals("") || cpwd.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Cannot be empty");
            }
            else
            {
                if(pwd.equals(cpwd))
                {
                    String sid = jTextField2.getText();
                    String query = "update tbstudent set stupwd = '" + pwd + "' where stuid = '" + sid + "'";
                    new DB().update(query);
                    
                    JOptionPane.showMessageDialog(null,"Password updated successfully!!");
                    StudentLogin l = new StudentLogin();
                    l.setVisible(true);
                    l.pack();
                    this.dispose();
                }

                else
                {
                    JOptionPane.showMessageDialog(null,"Password and Confirm Password should match");
                    jPasswordField1.setText("");
                    jPasswordField2.setText("");
                }
            }

        }

        catch(Exception e)
        {
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

