    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try
        {
            String sid = jTextField2.getText();
            if(!sid.equals(""))
            {
                String query = "select stusecques, stusecans from tbstudent where stuid = '" + sid + "'";
                DB obj = new DB();

                ResultSet rs = obj.execute(query);

                if(rs.next())
                {
                    jLabel3.setText(rs.getString("stusecques"));
                    organs = rs.getString("stusecans");
                    jPanel2.setVisible(true);
                    this.pack();
                    jTextField2.setEditable(false);
                }

                else
                {
                    JOptionPane.showMessageDialog(null,"Invalid Student ID");
                    jTextField2.setText("");
                }
            }

            else
            {
                JOptionPane.showMessageDialog(null,"Can't be empty");
            }

        }

        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

